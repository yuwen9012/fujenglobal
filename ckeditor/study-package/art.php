                <h3 class="my-3 fw-semibold">Faculty & Facilities</h3>
                <div class="justify-text">
                    <p class="my-3 fw-semibold" style="font-size: 20px;">1. Outstanding Faculty</p>
                    <li class="mt-3 fw-semibold">Diverse Professional Backgrounds:</li>
                    <span class="mt-3">The faculty covers fields such as music, visual arts, and landscape design. For example, the Music Department's faculty includes internationally renowned musicians and educators who provide instruction in music theory, performance techniques, and composition.</span>
                    <li class="mt-3 fw-semibold">Extensive Practical Experience:</li>
                    <div class="collapse" id="collapseArtFF">
                        <span class="mt-3"> Many faculty members have significant industry experience, combining theory and practice to offer students practical learning advice. For instance, the Applied Arts Department's faculty has extensive experience in animation, metalworking, and visual communication design, guiding students in their creative endeavors and participation in various design competitions.</span>
                        <li class="mt-3 fw-semibold">Landscape Architecture Experts:</li>
                        <span class="mt-3">The Landscape Architecture Department's faculty has backgrounds in landscape, architecture, urban planning, and more. For example, the current head, Professor Wen-Shan Huang, has profound expertise in landscape design and urban planning and actively promotes international exchanges and collaborations.</span>

                        <p class="my-3 fw-semibold" style="font-size: 20px;">2. Advanced Facilities</p>
                        <li class="mt-3 fw-semibold">Professional Studios and Laboratories:</li>
                        <span class="mt-3">The college is equipped with specialized studios and labs that provide spaces for student creation and experimentation. For instance, the Landscape Architecture Department has design studios and model-making workshops where students can engage in hands-on design and fabrication.</span>
                        <li class="mt-3 fw-semibold">High-Tech Equipment:</li>
                        <span class="mt-3">The college boasts advanced technology such as 3D printers, digital recording studios, and multimedia classrooms, supporting students in various media and technical creations. For example, the digital recording studio in the Applied Arts Department provides professional environments for music and applied arts students for recording, mixing, and production.</span>

                        <p class="my-3 fw-semibold" style="font-size: 20px;">3. Enhancing Global Competence and Innovation: A Spotlight on Fu Jen Catholic University's College of Arts</p>
                        <li class="mt-3 fw-semibold">International Music Exchange Activities in the Music Department:</li>
                        <span class="mt-3">The Music Department frequently hosts international masterclasses and workshops, inviting renowned musicians worldwide to guide students, enhancing their global perspectives and musical skills.</span>
                        <li class="mt-3 fw-semibold">Interdisciplinary Collaboration in the Applied Arts Department:</li>
                        <span class="mt-3">The Applied Arts Department collaborates with other faculties to offer interdisciplinary courses and workshops. For example, they partner with the Information Engineering Department to provide courses in digital interactive design, combining art and technology to expand students' creative horizons.</span>
                        <li class="mt-3 fw-semibold">Practical Training in the Landscape Architecture Department: </li>
                        <span class="mt-3">Students in the Landscape Architecture Department participate in real-world design and urban planning projects under faculty guidance and have opportunities to compete in international design competitions, enhancing their practical skills and global vision.</span>
