                <h3 class="my-3 fw-semibold">Learning Outcomes</h3>
                <div class="justify-text">
                    <p class="my-3">The Music Department aims to develop internationally competitive music talents with the help of full-time international faculty. The curriculum covers a wide range of music genres from classical to contemporary, including courses in music theory, modern music therapy, and digital music applications. English courses and international exchanges are offered to enhance students' global competitiveness. Regular concerts, workshops, and masterclasses are organized to improve students' performance and creativity. Additionally, the department actively collaborates with renowned international institutions, providing students with more learning opportunities and enhancing its international reputation.</p>
                    <div class="collapse" id="collapseArtLo">
                        <p class="my-3">Established in 1984, the Department of Applied Arts offers various courses in computer animation, metalworking, visual communication, and interior design. It focuses on high-quality, small-class teaching and expert faculty. The department established a Graduate Fine Arts program in 2001, which combines theory and practice. The Evening Division of the bachelor's degree program was introduced in 2010, offering tracks in Visual Communication and Metalworking. The department emphasizes strengthening moral ethics and creative thinking, integrating design theories with practice, cultivating local and international perspectives, and promoting group work and industry-academic cooperation.</p>
                        <p class="my-3">The Department of Landscape Architecture, established in 1989, offers both bachelor's and master's degree programs. Located in the College of Arts Building, the department benefits from excellent teaching space and resources. It maintains high academic standards and admission rates, admitting international students since the 101 academic year. The undergraduate program requires 132 credits and a graduation design, while the master's program requires 34 credits and a thesis defense. The department's leadership has contributed to its professional diversity and richness in teaching.</p>
