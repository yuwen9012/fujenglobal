                <h3 class="my-3 fw-semibold">Introduction</h3>
                <p class="my-3">The Department of Textiles and Clothing, College of Fashion and Textiles at Fu Jen University was founded in 1970 as the first bachelor’s program. Initially part of the College of Natural Sciences, it was incorporated into the newly established College of Human Ecology in 1993. In 1976 the Department created three majors which eventually became officially approved Sections. In response to the needs of our Taiwan textiles and clothing industry, and in close cooperation with it, the Department operates three Sections each of which develops young professionals who, upon their graduation, may optimistically look forward to a wide range of career opportunities. The three Sections are included: Textile Design Section, Fashion Design Section, and Fashion Marketing Section.</p>
                 <div class="collapse" id="collapseFtIntro">
                    <p class="my-3">Each Section offers its specialized curriculum with many opportunities for interaction and teamwork with students from other Sections, to simulate the actual way the industry operates. Studying and practicing in such an interactive environment allows students to develop an integrated understanding of the industrial processes and the ability to engage in cooperative approaches to problems and projects, both of which are indispensable for a successful career in this multifaceted industry.</p>
