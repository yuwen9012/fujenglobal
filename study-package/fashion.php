                <h3 class="my-3 fw-semibold">Features</h3>
                <p class="my-3">Our college focuses on the three sections of developing and offering the qualified and excellent potential candidates for fashion industry, included three sections: Textile Design Section, Fashion Design Section, and Fashion Marketing Section. Not just for internationally-oriented professional education for aspiring students, but also build up the talent students to develop their professional expertise and skills. We are committed to providing a high-quality learning environment, comprehensive study programs, career planning, and pathways for future academic advancement. Our college aiming to cultivate professionals who meet the demand of the apparel industry. The core courses of the apparel industry and the English major courses with a common foundation in apparel are cultivated to become design talents and brand marketing talents with basic general knowledge.</p>
                <div class="collapse" id="collapseFtFeatu">
                    <p class="my-3">Our students have basic knowledge of fashion and textiles, three different professional abilities and international outlook, excellent English speaking, and integrated practical skills in our college, established a connection between students and the industry through industry-academia cooperation, which can be said to be the leader of fashion University in Asia, and seamlessly integrate with the fashion and retail industry.</p>
                    <p class="my-3">We are committed to becoming an international fashion school and aim to provide an excellent learning environment for international students. Our College of Fashion and Textiles, fashion marketing division has launched English professional courses such as fashion merchandising and luxury brand management, brand marketing, and fashion media since 2021. The goal of education will be to become the top choice bilingual international fashion institute for international students, aligning with international fashion brand management.</p>
                    <p class="my-3">In the bilingual program, we actively create an international bilingual environment and courses that meet the needs of international brands and luxury brand-related industries with professional English standards. This allows students to learn brand management, brand marketing, fashion buying, professional English of apparel, and fashion media-related courses in a professional setting.</p>
                    <p class="my-3">Additionally, we offer overseas study programs in New York to achieve goals of professional development and employment training for international bilingual fashion managers. Our program seamlessly connects with the professional needs of the fashion industry and international brands, and we conduct student exchange programs with international brands and foreign fashion institutes to expand international academic exchanges and enhance students' global vision and capabilities. We also offer an all-English brand curriculum master's program as an important channel for cultivating professional managers in the fashion industry in the future. We offered the undergraduate program, Master Program Department of Textiles and Clothing, Graduate institute of Master program, Executive Master's Business Program (EMBA), Master Program of Brand and fashion Management,and Graduate Institute of Museum Studies to provide different choices for college students and professionals interested candidates in fashion.</p>
