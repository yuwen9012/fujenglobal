                <h3 class="my-3 fw-semibold">Introduction</h3>
                <div class="justify-text">
                    <p class="my-3">History of the College of Social Sciences, Fu Jen Catholic University</p>
                    <ul class="mb-0">
                        <li>1963: When Fu Jen Catholic University was re-established in Taiwan, the College of Law was founded, comprising the departments of Law, Economics, and Business Administration.</li>
                        <li>1969: The College of Law was expanded and divided into the College of Law and the College of Business. The departments of Economics, Business Management, and Accounting were placed under the College of Business, while the Department of Law and the newly established Department of Sociology were under the College of Law.</li>
                    </ul>
                    <div class="collapse" id="collapseSoIntro">
                    <ul>
                        <li>1972: The Department of Economics returned to the College of Law, and an evening bachelor's program in the Department of Economics was established.</li>
                        <li>1974: The Department of Law was divided into the Division of Legal Studies and the Division of Judicial Studies.</li>
                        <li>1977: The Department of Sociology was divided into the Division of Sociology and the Division of Social Work.</li>
                        <li>1979: The Department of Law added the Division of Financial and Economic Law.</li>
                        <li>1980: The Graduate Institute of Law was established, offering a master's program.</li>
                        <li>1981: The Division of Sociology and the Division of Social Work were reorganized into the Department of Sociology and the Department of Social Work, respectively.</li>
                        <li>1988: The Graduate Institute of Religious Studies was established.</li>
                        <li>1990: The Graduate Institute of Law established a doctoral program.</li>
                        <li>1992: The Graduate Institute of Economics and the Department of Religious Studies were established.</li>
                        <li>1993: The Center for Chinese Social and Cultural Studies was established.</li>
                        <li>1994: The Division of Financial and Economic Law in the Department of Law became an independent department, renamed the Department of Financial and Economic Law. The original Department of Law continued to have the divisions of Legal Studies and Judicial Studies. The Department of Religious Studies was reorganized into a bachelor's and master's program.</li>
                        <li>1998: The Department of Social Work established a master's program.</li>
                        <li>2000: The Department of Financial and Economic Law established a master's program.</li>
                        <li>2001: The Indigenous Peoples Service Center was established.</li>
                        <li>2002: An in-service master's program in the Department of Religious Studies was established, and the Peace Research Center was incorporated into the School of Law.</li>
                        <li>2003: The School of Law was restructured into the College of Social Sciences and the School of Law. The departments of Economics, Sociology, Social Work, Religious Studies, and the Indigenous Peoples Service Center were incorporated into the College of Social Sciences. The legal-related departments and the Peace Research Center became part of the School of Law. A credit program in Nonprofit Organization Management and a doctoral program in the Department of Religious Studies were established.</li>
                        <li>2004: The Department of Sociology established a master's program.</li>
                        <li>2007: The Center for Chinese Social and Cultural Studies was dissolved after completing its mission.</li>
                        <li>2009: The International Partnership Learning Promotion Center was established, and the Indigenous Peoples Service Center was renamed the Indigenous Education Center.</li>
                        <li>2010: An evening bachelor's program in the Department of Religious Studies was established.</li>
                        <li>2011: The Department of Psychology was incorporated into the College of Social Sciences. An in-service master's program in Nonprofit Organization Management and a bachelor's degree program in Catholic Studies were established.</li>
                        <li>2012: The Indigenous Education Center was renamed the Indigenous Social Development Studio.</li>
                        <li>2018: A credit program in Medical Dispute Communication and Resolution was established.</li>
                        <li>2019: A credit program in Life Education was established.</li>
                        <li>2021: The Center for Social Innovation and Nonprofit Organization Research was established.</li>
                    </ul>
