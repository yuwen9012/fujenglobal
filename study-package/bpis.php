                <h3 class="my-3 fw-semibold">Course Structure & Description</h3>
                <div class="justify-text">
                    <p class="my-3">Core courses:</p>
                    <p class="my-3 fw-semibold">Global Creative Industry</p>
                    <span>This course offers an introduction to the creative industries and the conditions that shape creative industries. The factors that shape the contemporary creative industry landscape include policy frameworks, socio-cultural, economic, and technological factors. </span>
                    <div class="collapse" id="collapseBpisCsd">
                        <span>In addition, how creative projects and products are created, funded, and circulated will be examined. Topics investigated include but are not limited to: creative entrepreneurship, creative economy, creative marketing, and cultural production system. Students will develop critical skills in assessing the contemporary operations of creative industries.</span>
                        <p class="mt-3 fw-semibold">International Relations</p>      
                        <p class="my-3">The goal of class:</p>
                        <ul class="decimal-list">
                            <li>
                                Analyze and understand the major themes of international relations and global politics
                            </li>
                            <li>
                                Develop an appreciation of theory and its utility in the study and practice of International Relations
                            </li>
                            <li>
                                 Improve critical thinking and writing skills
                            </li>
                            <li>
                                Demonstrate the ability to describe the social, political, and economic forces that influence social behavior and the global system
                                Use knowledge of international affairs in a practical problem-solving way to address issues of immediate international concern.
                            </li>
                        </ul>
                        <p class="mt-3 fw-semibold">Global Business Case Analysis</p>
                        <p class="my-3">This course will allow students to learn the fundamental theories and concepts of case study analysis and methods. In addition to the application of theoretical concepts, students will thoroughly assess the most appropriate action of the given situation and understand the ramifications of their decisions. Both international and local business cases of a particular business or management issue will be discussed. The categories of the case studies include: entrepreneurship, leadership, ethics, strategy, and sustainability case studies.</p>
