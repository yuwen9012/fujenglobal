                <h3 class="my-3 fw-semibold">Learning Outcomes</h3>
                <div class="justify-text">
                    <p class="mt-3 fw-semibold">Department of Chinese Literature</p>
                    <p class="my-3">To follow the mission of FJCU's holistic education, to strengthen students' knowledge and ability, to cultivate a human-centered spirit, to cultivate talents in Chinese studies, and to cultivate leaders to create new culture.</p>
                    <p class="mt-3 fw-semibold">Department of History</p>
                    <p class="my-3">To carry out the mission that FJCU joins Chinese and Western cultures, to cultivate students' international outlook and global vision, to inspire their independent thinking and research ability, to have a good professional foundation and human centered quality, and to encourage students to serve society and contribute what they have learned.</p>
                    <p class="mt-3 fw-semibold">Department of Philosophy</p>
                    <p class="my-3">To pay equal attention to both Chinese and Western philosophies, to integrate the spirit of Scholasticism, and to enhance the ability of rational reflection and criticism in studying philosophy, to adapt to the situation of modern society and life.</p>
