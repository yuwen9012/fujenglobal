                <h3 class="my-3 fw-semibold">Features</h3>
                <div class="justify-text">
                    <p class="mt-3 fw-semibold">Department of Physical Education:</p>
                    <p class="my-3">To offer discipline and technical courses, to combine theory and practice, to cultivate talents in sports competition and sports health management.</p>
                    <p class="mt-3 fw-semibold">Department of Library and Information Science:</p>
                    <p class="my-3">To train students to have the ability of library and information services, to integrate the application of science and technology, to cultivate a human-centered spirit, to promote knowledge integration and encourage students to actively participate in social services.</p>
                    
                    <div class="collapse" id="collapseEduFeatu">
                        <p class="mt-3 fw-semibold">Graduate Institute of Educational Leadership and Development:</p>
                        <p class="my-3">The courses attach importance to experience, teamwork and professional dialog, and emphasize leaders' attitudes, and the beliefs, values and practice of exemplary leadership behavior, thus demonstrating a leadership spirit.</p>
                        <p class="mt-3 fw-semibold">Bachelor’s Program in Sports and Leisure Management:</p>
                        <p class="my-3">Combined with the teaching resources of different departments, the program provides bachelor’s courses in professional knowledge, practical teaching leadership, and scientific and technological development.</p>
                        <p class="my-3">These courses construct students' interdisciplinary knowledge and ability so as to establish systematic thinking and problem- solving ability. They also contribute to diversified learning activities and enhance future competitiveness.</p>
                        <p class="mt-3 fw-semibold">Teacher Training Center:</p>
                        <p class="my-3">Taking specialty, care and service as the direction of curriculum design, this Center encourages innovative teaching. All teachers work together to develop problem-oriented learning strategies and to cultivate the students' ability of problem solving and self-directed learning. The Center also places importance on the students' team learning.</p>
                        <p class="mt-3 fw-semibold">Bachelor’s Program in Information Innovation and Digital Life:</p>
                        <p class="my-3">This Program is comprised of three major aspects, which complement each other:</p>
                        <ul class="custom-list">
                            <li>
                                Information innovation: mainly integrating the theoretical and practical application of information technology;
                            </li>
                            <li>
                                Digital Life: The combination of smart life technology products and multimedia technology, hoping to train students to become digital life design talents with creative thinking;
                            </li>
                            <li>
                                Information services: to cultivate students' basic knowledge and skills needed to engage in the information value chain, and to develop an ability in the characteristics of value- added information.
                            </li>
                        </ul>
